; r1 kunai's X , r2  kunai's Y, r3 the kunai sprite address  , r9 current level number, r8 number of lifes remaining
; ra naruto's X, rb naruto's Y, rc naruto's sprite address, rd fix the number off frames , re animation speed, rf inputs
; Left-Right naruto sprite is 34x55 px  , each movement is 6 frames
; Up-Down naruto sprite is 64x55 px  , each movement is 10 frames
; standing naruto sprite are 40x55 px --- 4 frames and  40x55 px --- 4 frames 
; falling naruto sprite are 23x57 px --- 3 frames and  44x76 px --- 9 frames
