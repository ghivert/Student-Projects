:startup
	cls ;clear the screen
	call draw_start_menu   ; display starting menu

;----Startup configuration-----
	cls ;clear the screen
	ldi ra, 0   	;naruto's X
	ldi rb, 185  	;naruto's Y
	ldi r9, 0 		; level 1
	ldi r8, 5      ; 5 lifes
	call load_level
;-----------------------------	

;--------------Main loop, Does nothing special but nothing will run without it ^_^ ------------
:start
	call stand   ; this is the real main loop, all calculus are made in this routine
	jmp start
	
:mission_completed
	call draw_end_menu  ; display end menu
	
:infinity  ; endless loop
	vblnk  
	jmp infinity
	
