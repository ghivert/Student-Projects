; ----------------------- Print text on screen ; Push ra and rb ,rc and r9 before using it, Pop after;---------------------------------		

:print_string			;Print string on screen
	ldi r5, font_data	;Pointer to the graphic data
	ldm r7, r6		;Get ascii code
	andi r7, 0xFF		;Use only low part of the data
	jz print_end		;If the char is null stop printing
	subi r7, 32		;Convert to char offset
	jc print_end		;If the char is below zero it's unprintable
	muli r7, 75		;Convert char offset to byte offset
	add r5, r7		;Get the char sprite address
	call copy_to_cache	;Copy char data to cache
	drw ra,rb,font_cache	;Draw char on screen
	addi ra, 8		;Increase X offset
	addi r6, 1		;Point to next char
	jmp print_string	;Print next char
:print_end
ret
:copy_to_cache
	stm ra,var_cache0	;Store register data
	stm rb,var_cache1
	stm r6,var_cache2
	ldi ra, 38		;Word counter
	ldi rb,font_cache	;Data cache pointer
:copy_loop
	ldm r6,r5		;Copy data to temp buffer
	and r6,rc		;Apply desired color to the sprite
	stm r6,rb		;Store modified data
	addi r5, 2		;Go to next word
	addi rb, 2		;Point to next word in data cache
	subi ra, 1		;Decrease counter
	jz copy_end		;Desired data has been cached, end process
	jmp copy_loop		;Go to next wor6
:copy_end
	ldm ra,var_cache0	;Recover register data
	ldm rb,var_cache1
	ldm r6,var_cache2
ret
:var_cache0
db 0x00
db 0x00
:var_cache1
db 0x00
db 0x00
:var_cache2
db 0x00
db 0x00


:draw_level
	push ra   	; store naruto's  X
	push rb		; store naruto's  Y
	push rc     ; store sprite address
	push r9     ; store current level number, used to caculate offset 
	push r8     ;store the number of lifes remaining, used to caculate offset
	
	flip 0,0   ; no flip
    spr 0x0F05		;10x15 px sprites    
	
	ldi r6, level    ;get "Level" address
	ldi ra, 5
	ldi rb, 1
	ldi rc, 0xEEEE   ;color light blue
	call print_string
	
	ldi r6, number   ; get numbers address
	muli r9,3  ; get the offset, each number is written in 3 bytes
	add r6,r9   ; point to real level number address
	addi r6,3   ; the numbers stars at 0 so must add 3 to point to 01 a least
	ldi ra, 60
	ldi rb, 1
	call print_string
	
	ldi r6, life    ;get "Lifes" address
	ldi ra, 245
	ldi rb, 1
	ldi rc, 0xDDDD   ;color blue
	call print_string
	
	
	ldi r6, number   ; get  number address
	muli r8,3  ; get the offset, each number is written in 3 bytes
	add r6,r8   ; point to real life number address (We don't have to substract with 3 because the number of remaining lifes can be 00)
	ldi ra, 300
	ldi rb, 1
	call print_string
	
	
	pop r8  ; last-in , first-out
	pop r9      
	pop rc
	pop rb
	pop ra
	ret

