;--------------------Collision Manager, Doesn't use pixel collision, otherwise the game will be too hard!!!!----------------------

:collision_manager

	:X_collision
	
		cmp ra,r1  ; compare naruto'S X-coordonate and the Kunai's X-coordonate
		jc naruto_X_under  ; Kunai's X is higher than Naruto's X
			
		:naruto_X_above     ; use this collision detection if naruto's X coordonate is higher than the kunai's X coordonate
	
		         sub ra,r1,r4   ; calculate the distance between naruto and the kunai
				 cmpi r4,15
				 jc Y_collision
				 jz Y_collision
				 ret
		
		
		:naruto_X_under  ; use this collision detection if naruto's X coordonate is lower than the kunai's X coordonate
			
				sub r1,ra,r4   ; calculate the distance between naruto and the kunai
				cmpi r4,15
				jc Y_collision
				jz Y_collision
				ret
				
	:Y_collision
	
		cmp rb,r2  ; compare naruto'S Y-coordonate and the Kunai's Y-coordonate
		jc naruto_Y_under  ; Kunai's Y is higher than Naruto's Y
			
		:naruto_Y_above     ; use this collision detection if naruto's Y coordonate is higher than the kunai's Y coordonate
		         sub rb,r2,r4   ; calculate the distance between naruto and the kunai
				 cmpi r4, 5
				 jc collision_detected
				 ret
				
		:naruto_Y_under  ; use this collision detection if naruto's Y coordonate is lower than the kunai's Y coordonate
				sub r2,rb,r4   ; calculate the distance between naruto and the kunai
				cmpi r4, 50     ; naruto is 55 px hight
				jc collision_detected
				ret
	
:collision_detected
	snd2 0x0080   
	call fall    ; play falling animation
	call draw_select_pause
	call wait_press  ; wait for a key is pressed
	snd2 0x0080
	cmpi r8,0  ; if the number of lifes is 0
	jz mission_failed
	
	subi r8,1    ; decrease the number of lifes
	jmp restart_level   ;  reset game before going to the main loop
	
;--------------------------------------------------------------------------------------------------------------
	
;--------------------------------------------------Level Manager-----------------------------------------------
:restart_level 
	cls ;clear the screen
	ldi ra, 0  ;naruto's X
	ldi rb, 185 ; naruto's Y
	ldi sp, 0xFDF0  ;reset stack
	call load_level
	jmp start    ;  go back to the beginning of the main loop 

	
:load_level
	ldi r1,70 ; counter , the memory map is 72 bytes , 70 bytes for kunais informations and 2 bytes to indicate the end of the memory map
	ldi r4, kunai_infos    ;get R/W address
	ldi r3, kunai_infos_map ; get Read only address
	push r9   ; store level number
	muli r9,72  ; offset for the indicated level memory map
	add r3,r9 ; get kunai infos starting address
	pop r9 ; get back the level number
	
	:start_load_level
		ldm r2,r3    ; get infos from Read only  memory map 
		stm r2,r4    ; store the info into Read and Write memory map 
		addi r3, 2  ; point to next information in Read only map
		addi r4, 2  ; point to next information in R/W memory
		subi r1, 2  ; decrease the counter
		jz end  
		jmp start_load_level


:next_level

	ldi rf, 0
	stm rf,0xFFF0   ; reset key status
	
	addi r9,1  ; go to next level
	cmpi r9,max_level  ; check if the game is finished
	jz mission_completed
	snd1 0x0050  ; play sound
	cls ;clear the screen
	ldi ra, 0  ; naruto's X
	ldi rb, 185 ; naruto's Y
	ldi sp, 0xFDF0  ;reset stack
	call load_level  ; copy the level informations
	jmp start

:mission_failed
	snd3 0x00FF  ; play sound
	ldi sp, 0xFDF0 ; reset stack
	jmp startup   ; go back to the beginning of the rom
;------------------------------------------------------------------------------------------------------
	
; ----------------------- Routines for making pauses and return from subroutines-----------------------
:pause
	ldi re, 4 
	
	:start_pause
	vblnk
	subi re, 1
	jz end
	jmp start_pause	
	
:end
	ret
