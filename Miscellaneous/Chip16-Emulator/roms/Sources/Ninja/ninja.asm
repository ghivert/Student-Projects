;----------------------------------------Manage all user inputs----------------------------------------------	
:input_listener
	
	ldm rf, 0xFFF0   ; get the controller 1 status
	
	tsti rf, 0x0010  ; get select status
	cnz wait_press    ;select is pressed then make a pause
	
	tsti rf, 0x01  ; get key up status
	jnz go_up    ;up is pressed
	
	tsti rf, 0x02  ; get key down status
	jnz go_down    ;up is pressed
	
	tsti rf, 0x04  ; get key left status
	jnz go_left    ;up is pressed
	
	tsti rf, 0x08  ; get key right status
	jnz go_right    ;up is pressed

	jmp end ; no key is pressed

:wait_press
	ldi rf, 0
	stm rf,0xFFF0   ; reset key status
	
	vblnk   ; raise vblank to let the user update key status
	
	ldm rf, 0xFFF0   ; get the controller 1 status
	tsti rf, 0x1F  ; get keys status
	jnz wait_finished 
	jmp wait_press

		:wait_finished
		ldi rf, 0
		stm rf,0xFFF0  ; reset keys' status
		call pause ; let the user be prepared
		ret
