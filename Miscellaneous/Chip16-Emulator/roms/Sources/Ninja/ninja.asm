;-------------------------------------Animation routines --------------------------------------------
:go_right
	cmpi ra, 320  ; check collision with right border of the screen
	jz end  
	
	ldi rc, naruto_lr ; starting sprite for right animation 
	ldi rd, 6  ; 6 frames to go right
	
		start_go_right:
		flip 0,0 ; no flip
		spr 0x3711    ; set the sprite to 34x55 px
		call draw_naruto ;display the sprite on screen
		subi rd, 1     ; decrease number of frames
		jz end
		addi ra, 8 ; go 8 pixels right
		addi rc, naruto_lr_spr_size ; point to next frame
		jmp start_go_right ; draw next frame
		
:go_left
	cmpi ra, 0  ; check collision with left border of the screen
	jz end  
	
	ldi rc, naruto_lr ; starting sprite for left animation 
	ldi rd, 6  ; 6 frames to go left, starting a 0
	
		start_go_left:
		flip 1,0 ; horizontal flip
		spr 0x3711    ; set the sprite to 32x55 px
		call draw_naruto ;display the sprite on screen
		subi rd, 1     ; decrease number of frames
		jz end
		subi ra, 8 ; go 8 pixels left
		addi rc, naruto_lr_spr_size ; point to next frame
		jmp start_go_left ; draw next frame

:go_up  ; this routine is devided in 2 parts: in the first one: naruto's Y is decrease in the other it doesn't change
    cmpi rb, 17  ; check collision with up border of the screen , using the first 17 pixels to display the game level and remaining lifes
	jz end  

	ldi rc, naruto_ud ; starting sprite for up/down animation 
	ldi rd, 5 ; 5 frames to go up 
	
		start_go_up_1:
		flip 0,0 ; no flip
		spr 0x3720   ; set the sprite to 55x64 px
		call draw_naruto  ;display the sprite on screen
		subi rd, 1     ; decrease number of frames
		jz intermediate_up
		subi rb, 7 ; go 7 pixels up
		addi rc, naruto_ud_spr_size ; point to next frame
		jmp start_go_up_1 ; draw next frame
	
:intermediate_up  ; in this routine naruto's Y doesn't change
	ldi rd, 5 ; 5 frames to go up , starting a 0
	
		start_go_up_2:
		flip 0,0 ; no flip
		spr 0x3720   ; set the sprite to 55x64 px
		call draw_naruto ;display the sprite on screen
		subi rd, 1     ; decrease number of frames
		jz end
		addi rc, naruto_ud_spr_size ; point to next frame
		jmp start_go_up_2 ; draw next frame

		
:go_down  ; same as go up
	cmpi rb, 185  ; check collision with down border of the screen
	jz end  
	
	ldi rc, naruto_ud ; starting sprite for up/down animation 
	ldi rd, 5 ; 5 frames to go down
	
		start_go_down_1:
		flip 0,0 ; no flip
		spr 0x3720   ; set the sprite to 55x64 px
		call draw_naruto
		subi rd, 1     ; decrease number of frames
		jz intermediate_down
		addi rb, 7 ; go 7 pixels down
		addi rc, naruto_ud_spr_size ; point to next frame
		jmp start_go_down_1 ; draw next frame
	
:intermediate_down
	ldi rd, 5 ; 5 frames to go down
	
		start_go_down_2:
		flip 0,0 ; no flip
		spr 0x3720   ; set the sprite to 55x64 px
		call draw_naruto
		subi rd, 1     ; decrease number of frames
		jz end
		addi rc, naruto_ud_spr_size ; point to next frame
		jmp start_go_down_2 ; draw next frame
		
:stand ; this routine is called when there is no key pressed
	ldi rc, naruto_stand ; starting sprite for stand animation 
	ldi rd, 4 ; 4 frames 
	
		start_stand_1:
		flip 0,0 ; no flip
		spr 0x3714   ; set the sprite to 40x55 px
		call input_listener
		tsti rf, 0x1F  ; get keys status   ( only check Left-Right-Up-Down and Select), if the user press a key, the animation is stopped
		jnz stand   ; restart animation if a key has been pressed
		call draw_naruto
		subi rd, 1     ; decrease number of frames
		jz intermediate_stand
		addi rc, naruto_stand_spr_size ; point to next frame
		jmp start_stand_1 ; draw next frame
	
:intermediate_stand
	ldi rd, 4 ; 4 frames , starting a 0
	
		start_stand_2:
		flip 0,0 ; no flip
		spr 0x3714   ; set the sprite to 40x55 px
		call input_listener
		tsti rf, 0x1F  ; get keys status   ( only check Left-Right-Up-Down and Select), if the user press, the animation is stopped
		jnz stand        ; restart animation if a key has been pressed
		call draw_naruto
		subi rd, 1     ; decrease number of frames
		jz end
		subi rc, naruto_stand_spr_size ; point to next frame
		jmp start_stand_2 ; draw next frame	

:fall
	ldi rc, fall_1
	spr 0x3617  ; set the sprite to 54x46 px
	ldi rd,3 ; set the number of frames
	
		:start_fall_1
		cls
		bgc 3
		drw ra,rb,rc
		call pause 
		subi rd, 1     ; decrease number of frames
		jz intermediate_fall
		addi rc, naruto_fall_1_spr_size ; point to next frame
		jmp start_fall_1 ; draw next frame	
	
:intermediate_fall
	ldi rc, fall_2
	spr 0x2C26  ; set the sprite to 44x76 px
	ldi rd,9 ; set the number of frames
	
		:start_fall_2
		cls
		bgc 3
		drw ra,rb,rc
		call pause 
		subi rd, 1     ; decrease number of frame
		jz end
		addi rc, naruto_fall_2_spr_size ; point to next frame
		jmp start_fall_2 ; draw next frame	
	
;-----------------------------------------------------------------------------------------------------


;--------------------------------Draw naruto on the screen, Check collision and level up---------------	
:draw_naruto	
	cls    ; clear previous sprite
	drw ra,rb,rc  ; display the sprite on screen, must display naruto first before background because the sprite width and height for naruto are defined ealier
	
	call display_kunai   ; this routine draw all kunai's on screen
	
	cmpi ra,0x140  ; if naruto has reached the right border
	jz next_level  ; go to next level
	
	call draw_level ; this routine displays the game level and the number of lifes remaning
	
	call pause
	
    ret
;-----------------------------------------------------------------------------------------------------



 ;----------------------------------Draw all kunai's on screen-------------------------------------------
:display_kunai
	flip 0,0 ; no flip
	spr 0x0A19
	ldi r3, kunai_infos
	
		:start_display_kunai
		ldm r1,r3    ; get the kunai status : end of memory map 	
		cmpi r1,0x00FF ; check if we are at the end of memory map
		jz end
		andi r1, 0xFF ;  get the 8 Less Significant bytes  only
		cmpi r1,1    ; check the kunai status : ON or OF
		jnz next_kunai
		addi r3,1 ; point to X
		ldm r1,r3 ; get X
		addi r3,2 ; point to Y
		ldm r2,r3  
		andi r2, 0x00FF ;  get the 8 Less Significant bytes  only for Y
		drw r1,r2, kunai ;display kunai
		call collision_manager   ; check if the kunai has touched Naruto
		addi r3,1 ; point to speed
		ldm r2,r3
		andi r2,0x00FF  ; get speed, can use r2 because Y coordonate don't change
		subi r3,3 ; point to x address
		sub r1,r2 ; point X to next address of the kunai according to his speed
		stm r1,r3 ; store new X coordonate
		cz recall_kunai  ; if the Kunai goes out, we recall it
		cn recall_kunai  ; if the Kunai goes out, we recall it   (can be zero or negative according to his speed)
		addi r3,4 ; point to next Kunai address
		jmp start_display_kunai
		
			:next_kunai
			addi r3,5 ; point to next kunai infos in kunai memory map
			jmp start_display_kunai
		
:recall_kunai
	ldi r2,0x0170    ; put the kunai far than 320 to avoid collision when naruto is getting out of the screen  0x0170= 368, otherwise you may have bad surprises ^_^
	stm r2,r3  ; reset X coordonate of the kunai
	ret
	
