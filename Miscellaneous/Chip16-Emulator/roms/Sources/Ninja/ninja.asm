;------------------------Define all constants for sprites animations and the game levels-----------------------
naruto_lr_spr_size 		equ 935  ;each frame occupy 935 byte in memory, sprite_width*sprite_height/2
naruto_ud_spr_size 		equ 1760 ;each frame occupy 1760 byte in memory, sprite_width*sprite_height/2
naruto_stand_spr_size 	equ 1100 ;each frame occupy 1100 byte in memory, sprite_width*sprite_height/2
naruto_fall_1_spr_size 	equ 1242 ;each frame occupy 1242 byte in memory, sprite_width*sprite_height/2
naruto_fall_2_spr_size 	equ 1672 ;each frame occupy 1672 byte in memory, sprite_width*sprite_height/2
max_level 				equ 15 	; there are 15 levels
